    /** Returns the mime type for the given extension, defaults to "text/html" if no matching mime type was found */
    var value: String {
        guard let ext = ext else { return defaultMimeType }
        return MimeTypes.extensions[ext.lowercased()] ?? defaultMimeType
    }

    /**
     Returns the extension for a given mime type

     - parameter mimeType: mime type, e.g. "text/html"
     - returns: extension or nil if none was found
     */
    static func getExtension(for mimeType: String) -> String? {
        return MimeTypes.extensions.first { $0.value == mimeType }?.key
    }

